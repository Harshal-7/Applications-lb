///////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Multiplication
//  Description:    Used to perform Multiplication of 2 numbers
//  Input:          Float,Float
//  Output:         Float
//  Date:           15/04/2022
//  Author:         Harshal Ramdas Shinde
//
///////////////////////////////////////////////////////////////////////////

float Multiplication(float fValue1, float fValue2)
{
    float fAns1 = 0.0;
    fAns1 = fValue1 * fValue2;
    return fAns1;
}

///////////////////////////////////////////////////////////////////////////
//  
//  Function Name:  Division
//  Description:    Used to perform Division of 2 Numbers
//  Input:          Float,Float
//  Output:         Float
//  Date:           15/04/2022
//  Author:         Harshal Ramdas Shinde  
//
///////////////////////////////////////////////////////////////////////////


float Division(float fValue1, float fValue2)
{
    float fAns2 = 0.0;
    fAns2 = fValue1 / fValue2;
    return fAns2;
}